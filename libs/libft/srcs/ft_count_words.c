#include "libft.h"

size_t	ft_count_words(char const *s, char c)
{
	size_t	i;

	i = 0;
	while (*s)
	{
		if (*s != c && (*(s + 1) == c || !*(s +1)))
			i++;
		s++;
	}
	return (i);
}
