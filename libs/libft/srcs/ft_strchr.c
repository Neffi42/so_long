#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	ch;
	int		i;

	ch = c;
	i = 0;
	while (((char *)s)[i])
	{
		if (ch == ((char *)s)[i])
			return (((char *)s) + i);
		i++;
	}
	if (ch == ((char *)s)[i])
		return (((char *)s) + i);
	return (NULL);
}
