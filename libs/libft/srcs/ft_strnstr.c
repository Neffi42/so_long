#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;
	size_t	size_l;

	if (!big && !len)
		return (NULL);
	i = 0;
	size_l = ft_strlen(little);
	if (!size_l)
		return ((char *)(big));
	while (big[i] && i < len)
	{
		j = 0;
		while (little[j] && big[i + j] == little[j] && i + j < len)
			j++;
		if (j == size_l)
			return ((char *)(big + i));
		i++;
	}
	return (NULL);
}
